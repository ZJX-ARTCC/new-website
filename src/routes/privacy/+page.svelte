    Open Authorization Scheme (OAuth), VATSIM and VATUSA provides information to Jacksonville ARTCC in order to 
    facilitate your usage of our services. When logging in via OAuth, the information which is provided to our 
    services is explicitly detailed and you must consent to this information being shared with us prior to 
    logging in. Demographic information may be collected to provide targeted services and improve system security
    (including location, number of sessions, and session duration).
  </p>
  <p>
    Some parts of our systems are accessible without registration or authentication. Despite not being required 
    to log in to these services with personal credentials, data will be logged. Data includes cookies, IP addresses, 
    and other data which may result in you being identifiable. If you wish to utilize our services while retaining anonymity, 
    this is possible by taking appropriate anonymisation measures within your browser's settings.
